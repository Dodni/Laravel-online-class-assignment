						<h1 class="entry-title">Research project</h1>
					</header><!-- .entry-header -->

					<div class="entry-content">
						<p>Research commissioned by ERSTE Foundation to assess the impact of the percentage tax designation on civil society based on experience from Hungary, Lithuania, Poland, Slovakia and Romania.</p>
<h2 style="text-align: center;">Research project<br />
Assessment of the impact of the percentage tax designation concept and systems with recommendations for future policies</h2>
<p>The percentage tax designation concept (sometimes referred to as tax assignation or percentage tax giving) emerged in the policy debate in the late 1990s and materialised first in Hungary (1996), later in Slovakia, Romania, Lithuania and Poland, as an innovative mechanism that channeled public funds in a decentralized way to public benefit purposes. Today, in each country it has a slightly different form, but the core principle is the same: it grants a right to a taxpayer to designate 1% or 2% of paid income tax to a non-profit, non-governmental organization or other type of public benefit entity or to a church.</p>
<p>Today, almost twenty years after the introduction of the mechanism in Hungary and 10 years after the latest and only comparative work, a need emerges on evaluating the mechanism towards its original ambition: to critically assess its value and its impact i.e. to evaluate the role of the mechanism in the support the civil society and in the development of philanthropic culture from a more longer-term perspective.</p>
<p>The lessons to be learnt from such effort could be instrumental and useful to policy makers, donors and civil society actors not only in the countries that currently use the mechanism, but also in other countries that look for innovative ways of public funding towards civil society or otherwise strengthening the civil society.</p>
<p>The project will produce a regional synthesis report on the percentage tax designation experience in the CEE region and offer its findings to interested public and research and policy community in early 2016.</p>
<p>The main focus of the research will be based on the findings from countries that have already multiple years of experience with the mechanism – Hungary, Lithuania, Poland, Romania and Slovakia.  It will also tap into the experience of countries that considered the percentage tax designation mechanism but did not embrace it (e.g. Czech Republic, Estonia, Ukraine). The project will interact with policy experts from countries that show interest in implementing the system.</p>
<p>The Project will work with a local pool of experts utilizing the in-country knowledge to be applied to a cohesive work of a synthesized research report. A second phase of the project is under planning for an empirical research (funding dependent) and a third phase where policy development assistanse could be provided for interested countries (interest and funding dependent).</p>
<p class="p1"><span class="s1">Commissioned by ERSTE Foundation, the Project is implemented by an international team of researchers under the coordination of Dr. Marianna Török and Mr. Boris Strečanský from the Center for Philanthropy in Bratislava.</span></p>
<p>The project is 12 months long (starting March 2015). Local project partners were identified by the end of March 2015 and asked to contribute to the work with their local expertise in the design and completion of the project.</p>
<p>The result of the work will be country reports in English and an assessment report in English with its summary translated into several languages. Dissemination of project results is planned to take place on the website of the project and presented as an open source material.</p>
<p>In addition, articles, public presentations and conference participation are also planned.</p>
