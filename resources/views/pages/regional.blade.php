<h2><a href="http://taxdesignation.org/wp-content/uploads/2016/09/Tax_Percentage_Study_Web.pdf">Assessment of the Impact of The Percentage Tax Designations: Past, Present, Future</a></h2>
<pre><strong>Editors</strong>

Boris Strečanský

Marianna Török



</pre>
<h6><a name="_Toc330649325"></a>Executive summary</h6>
<p><strong> </strong>The subject of this study is the percentage tax designation system as a phenomenon in the nexus of public finance allocation, public benefit/civil society realm and taxation. Its focus is Central and Eastern Europe (CEE), where the system has remained to be a popular policy instrument over twenty years.</p>
<p>This research had been prepared in 2014 and was conducted during 2015 in five CEE countries that use the percentage tax designation system: Hungary, Lithuania, Poland, Romania and Slovakia. It focused on four areas: 1) What the percentage tax designation system actually is and what it is not, 2) What is its role in funding of the non-profit sector, 3) What are its side effects and 4) What is the connection between the policy making and the results?</p>
<p>The research had a form of expert panel through country research associates that worked according to the unified methodology, using secondary quantitative and qualitative data. The data including the expert opinion were organized into the project’s database.</p>
<p>The percentage mechanism was met with enthusiasm in the transitional phase of post-communist Central and Eastern Europe. It has offered a unique way of redistribution of state resources to public benefit activities in an environment that was resource dry with bureaucratic and un-transparent, politically biased public funding mechanisms. The percentage tax designation system works as a decentralised decision making mechanism where state resources, namely certain percentages of the income tax, are channelled, mostly to not-for-profit organizations (as well as other public and private entities with (mostly) public benefit purposes), based on the decision of the taxpayers and therefore reflecting the societal needs as perceived by taxpayers.</p>
<p>While this system is often referred to as “percentage philanthropy”, it is wrong to call it “philanthropy” as the resources used are not private resources, but resources that must be paid anyway as income tax. It is also wrong to assume that the mechanism supports the non-profit sector at large, as not only, and not all not-for-profit organizations benefit from the percentage mechanism. Nevertheless it is true that the system aims to support the public good.</p>
<p>The procedure, and at the same time the key feature, of the mechanism is that a taxpayer communicates its decision regarding the chosen beneficiary of the income tax percentage allocation to the tax authority and based on that, the beneficiary entity receives the financial designation together with other designations to be used in the given year- is the same throughout the years and countries. Small technicalities of the operation vary from one country to another and adjustments in the processes and procedures can influence the outcome.</p>
<p>The review of the perceived effects and intended policies suggests, that today, in many of the percentage club countries, there is a modest but distinctive contribution of the mechanism towards the sustainability of the public benefit organizations, especially the NGOs.</p>
<p>The percentage mechanism has been noted as an important source of revenue to many organizations that is estimated to have provided around € 5 billion support to a variety of beneficiaries in Europe over the years. Still, this source is a small portion of the overall revenues of the non-profit sectors (around 2%). At the same time, it is an important and often only source of revenue to many non-profit organizations in the region. The system has grown over time in terms of the number of beneficiaries and amounts they receive. Crowding out of state and private resources can not be observed (the latter point is also supported by the cases of Poland and Hungary) even though incentives for individual private giving have lessened in most countries.</p>
<p>Besides the area of financial viability, the mechanism has produced visible benefits and added value to civil society organizations in all percentage club countries in the area of public image as visibility has increased and communications skills have developed. The flexibility and predictability of the mechanism has contributed to the stability of the third sector. This unique way of channelling public funds has provided state resources to recipients that would otherwise have limited access to them. In addition, in all of the studied countries positive changes occurred in the associative dimension of civil society and in philanthropic activities, but these can not necessarily be attributed to the percentage mechanism.</p>
<p>There have been some unintended effects that raise point of concern. On the level of understanding of the concept, there has been an ongoing misunderstanding of the percentage tax designation system with philanthropy. One of the rather negative side effects or unrelated occurrences of the percentage designation from the perspective of the sustainability of NGOs has been the abolishment of fiscal incentives for giving in several countries.</p>
<p>The systematic policy evaluation regarding the percentage tax designation mechanism is lacking in most countries. The system does not stagnate: changes and adjustments are frequent; the proportion of taxpayers using the system, the amounts designated, the number of beneficiaries has been growing (with a slower speed and sometimes small decline in some countries in recent years). The system that once was an innovative policy solution has become part of the in-country status quo that the societal actors live with and enjoy the benefits of. The system continues to operate in all countries and it is found to be used in new ways in these countries. In addition, after twenty years of the percentage tax mechanism was started in Central and Eastern Europe, a new country (Moldova) has enacted a percentage legislation in 2015.</p>
